class Pad(object):
    """PadMultiView the image & mask.

    There are two padding modes: (1) pad to a fixed size and (2) pad to the
    minimum size that is divisible by some number.
    Added keys are "pad_shape", "pad_fixed_size", "pad_size_divisor",

    Args:
        size (tuple, optional): Fixed padding size.
        size_divisor (int, optional): The divisor of padded size.
        pad_val (float, optional): Padding value, 0 by default.
    """

    def __init__(self, size=None, size_divisor=None, pad_val=0):
        self.size = size
        self.size_divisor = size_divisor
        self.pad_val = pad_val
        # only one of size and size_divisor should be valid
        assert size is not None or size_divisor is not None
        assert size is None or size_divisor is None

    def _pad_img(self, results):
        """PadMultiView images according to ``self.size``."""
        for key in ['img', 'img_mask']:
            if key not in results:
                continue

            if self.size is not None:
                img = mmcv.impad(
                    results[key], shape=self.size, pad_val=self.pad_val)
            elif self.size_divisor is not None:
                img = mmcv.impad_to_multiple(
                    results[key], self.size_divisor, pad_val=self.pad_val)
            else:
                img = results[key]

            results[key] = img
            results['pad_shape'] = img.shape
            results['pad_fixed_size'] = self.size
            results['pad_size_divisor'] = self.size_divisor

    def __call__(self, results):
        """Call function to pad images, masks, semantic segmentation maps.

        Args:
            results (dict): Result dict from loading pipeline.

        Returns:
            dict: Updated result dict.
        """
        self._pad_img(results)
        return results

    def __repr__(self):
        repr_str = self.__class__.__name__
        repr_str += f'(size={self.size}, '
        repr_str += f'size_divisor={self.size_divisor}, '
        repr_str += f'pad_val={self.pad_val})'
        return repr_str


class PadMultiView(object):
    """PadMultiView the image & mask.

    There are two padding modes: (1) pad to a fixed size and (2) pad to the
    minimum size that is divisible by some number.
    Added keys are "pad_shape", "pad_fixed_size", "pad_size_divisor",

    Args:
        size (tuple, optional): Fixed padding size.
        size_divisor (int, optional): The divisor of padded size.
        pad_val (float, optional): Padding value, 0 by default.
    """

    def __init__(self, size=None, size_divisor=None, pad_val=0):
        self.size = size
        self.size_divisor = size_divisor
        self.pad_val = pad_val
        # only one of size and size_divisor should be valid
        assert size is not None or size_divisor is not None
        assert size is None or size_divisor is None

    def _pad_img(self, results):
        """PadMultiView images according to ``self.size``."""

        max_h = max([img.shape[0] for img in results['img']])
        max_w = max([img.shape[1] for img in results['img']])
        for key in ['img', 'img_mask']:
            if key not in results:
                continue

            imgs = []
            pad_shapes = []
            for img in results[key]:
                if self.size is not None:
                    img = mmcv.impad(
                        img, shape=self.size, pad_val=self.pad_val)
                elif self.size_divisor is not None:
                    # img = mmcv.impad_to_multiple(
                    #     img, self.size_divisor, pad_val=self.pad_val)
                    pad_h = int(np.ceil(max_h / self.size_divisor)) * self.size_divisor
                    pad_w = int(np.ceil(max_w / self.size_divisor)) * self.size_divisor
                    img = mmcv.impad(
                        img, shape=(pad_h, pad_w), pad_val=self.pad_val)
                imgs.append(img)
                pad_shapes.append(img.shape)

            results[key] = imgs
            results['pad_shape'] = pad_shapes
            results['pad_fixed_size'] = self.size
            results['pad_size_divisor'] = self.size_divisor

    def __call__(self, results):
        """Call function to pad images, masks, semantic segmentation maps.

        Args:
            results (dict): Result dict from loading pipeline.

        Returns:
            dict: Updated result dict.
        """
        self._pad_img(results)
        return results

    def __repr__(self):
        repr_str = self.__class__.__name__
        repr_str += f'(size={self.size}, '
        repr_str += f'size_divisor={self.size_divisor}, '
        repr_str += f'pad_val={self.pad_val})'
        return repr_str


class ToTensor(object):
    """Default formatting bundle.

    It simplifies the pipeline of formatting common fields for voxels,
    including "proposals", "gt_bboxes", "gt_labels", "img_mask" and
    "gt_semantic_seg".
    These fields are formatted as follows.
    """

    def __init__(self,
                 keys,
                 meta_keys=('filename', 'ori_shape', 'img_shape', 'pad_shape', 'bev_shape', 'scale_factor',
                            'flip', 'img_norm_cfg', 'sample_idx', 'cam_id', 'calibration',
                            'rot_angle', 'image_offset', 'class_dict')):
        self.keys = keys
        self.meta_keys = meta_keys

    def __call__(self, results):
        """Call function to transform and format common fields in results.

        Args:
            results (dict): Result dict contains the data to convert.

        Returns:
            dict: The result dict contains the data that is formatted with
                default bundle.
        """
        for key in self.keys:
            if key not in results:
                continue
            if key in ['img', 'img_mask', 'bev_mask']:
                if len(results[key].shape) == 3:
                    results[key] = np.ascontiguousarray(results[key].transpose(2, 0, 1))
            results[key] = to_tensor(results[key])

        data = {}
        img_metas = {}
        for key in self.meta_keys:
            if key not in results:
                continue
            img_metas[key] = results[key]

        data['img_metas'] = img_metas
        for key in self.keys:
            if key not in results:
                continue
            data[key] = results[key]
        return data

    def __repr__(self):
        """str: Return a string that describes the module."""
        return self.__class__.__name__ + \
               f'(keys={self.keys}, meta_keys={self.meta_keys})'


